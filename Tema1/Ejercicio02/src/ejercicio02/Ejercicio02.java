public class Ejercicio02 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int dato1 = 20;
        int dato2 = 10; 
        int dato3 = 5;
        
        // Hacemos la operacion de sumar
        int resultado = dato1 + dato2 + dato3; 
        System.out.println(dato1 + " + " + dato2 + " + " + dato3 + " = " + resultado);
        
        // Hacemos la operacion de restar
        resultado = dato1 - dato2 - dato3;
        System.out.println(dato1 + " - " + dato2 + " - " + dato3 + " = " + resultado);
        
        // Hacemos la operacion de multiplicar
        resultado = dato1 * dato2 * dato3;
        System.out.println(dato1 + " * " + dato2 + " * " + dato3 + " = " + resultado);
        
        // Hacemos la operacion de dividir
        resultado = dato1 / dato2 / dato3;
        System.out.println(dato1 + " / " + dato2 + " / " + dato3 + " = " + resultado);
    }
    
}
