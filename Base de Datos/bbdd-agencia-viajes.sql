CREATE DATABASE AGENCIA_VIAJES;
USE AGENCIA_VIAJES;

-- 1. Creación de la tabla empleados
CREATE TABLE empleados (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR(100) NOT NULL,
    PUESTO VARCHAR(50) NOT NULL
);

-- 2. Creación de la tabla alojamientos
CREATE TABLE alojamientos (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR(100) NOT NULL,
    LOCALIDAD VARCHAR(50) NOT NULL,
    PRECIO_NOCHE DECIMAL(6, 2) NOT NULL
);

-- 3. Creación de la tabla clientes
CREATE TABLE clientes (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    NOMBRE VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL,
    TELEFONO VARCHAR(15)
);

-- 4. Creación de la tabla reservas (Tabla central con las relaciones)
CREATE TABLE reservas (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    ID_CLIENTE INT NOT NULL,
    ID_EMPLEADO INT NOT NULL,
    ID_ALOJAMIENTO INT NOT NULL,
    FECHA_ENTRADA DATE NOT NULL,
    FECHA_SALIDA DATE,  -- CAMPO NO OBLIGATORIO (sin NOT NULL)
    
    -- Definición de las Claves Foráneas con CASCADE
    FOREIGN KEY (ID_CLIENTE) REFERENCES clientes(ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (ID_EMPLEADO) REFERENCES empleados(ID) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (ID_ALOJAMIENTO) REFERENCES alojamientos(ID) ON DELETE CASCADE ON UPDATE CASCADE
);

