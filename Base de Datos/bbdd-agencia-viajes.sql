-- TRIGGER  1: -- Crear un trigger que registre en una tabla de log cada vez que cambie el precio por noche de un alojamiento. 
CREATE TABLE LOG_CAMBIOS_PRECIO (
    ID_LOG INT PRIMARY KEY AUTO_INCREMENT,
    ID_ALOJAMIENTO INT,
    PRECIO_ANTIGUO DECIMAL(6, 2),
    PRECIO_NUEVO DECIMAL(6, 2),
    FECHA_CAMBIO DATETIME
);

DESC LOG_CAMBIOS_PRECIO;

DELIMITER //
DROP TRIGGER IF EXISTS TRG_LOG_CAMBIO_PRECIO //
CREATE TRIGGER TRG_LOG_CAMBIO_PRECIO
BEFORE UPDATE ON ALOJAMIENTOS
FOR EACH ROW
BEGIN
	IF OLD.PRECIO_NOCHE != NEW.PRECIO_NOCHE THEN
		INSERT INTO LOG_CAMBIOS_PRECIO(ID_ALOJAMIENTO,PRECIO_ANTIGUO,PRECIO_NUEVO,FECHA_CAMBIO) 
        VALUES (OLD.ID,OLD.PRECIO_NOCHE,NEW.PRECIO_NOCHE, current_date());
	END IF;
END; //
 DELIMITER ;
 
